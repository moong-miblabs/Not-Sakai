export default function encode (payload) {
	if(typeof payload != 'string') return payload
	let arr_payload = payload.split('')
	let encoded = arr_payload.map((value) => {
		if(alpha.hasOwnProperty(value)) return alpha[value]
		else return value
	})
	return encoded.join('')
}